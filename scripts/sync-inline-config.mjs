#!/usr/bin/env node
// Embeds platforms.json into index.html as an inline fallback so the app still
// works when opened via file:// (where fetch of ./platforms.json is blocked
// by CORS). In production (Netlify / any same-origin host), the external
// platforms.json is fetched and takes precedence, so edits there still go live
// without a rebuild.
//
// Usage: node scripts/sync-inline-config.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const jsonPath = resolve(root, "platforms.json");
const htmlPath = resolve(root, "index.html");

const MARKER_START = "<!-- INLINE_CONFIG_START (auto-generated by scripts/sync-inline-config.mjs — do not hand-edit) -->";
const MARKER_END = "<!-- INLINE_CONFIG_END -->";

const raw = readFileSync(jsonPath, "utf8");
const parsed = JSON.parse(raw); // throws on invalid JSON
const minified = JSON.stringify(parsed);

const block =
  MARKER_START + "\n" +
  '  <script type="application/json" id="inlinePlatformsConfig">' + minified + "</script>\n" +
  "  " + MARKER_END;

let html = readFileSync(htmlPath, "utf8");

const startIdx = html.indexOf(MARKER_START);
const endIdx = html.indexOf(MARKER_END);

if (startIdx === -1 || endIdx === -1 || endIdx < startIdx) {
  console.error("Could not find INLINE_CONFIG markers in index.html. Aborting.");
  process.exit(1);
}

const before = html.slice(0, startIdx);
const after = html.slice(endIdx + MARKER_END.length);
const updated = before + block + after;

if (updated === html) {
  console.log("index.html inline config already up to date.");
} else {
  writeFileSync(htmlPath, updated);
  const bytes = Buffer.byteLength(minified, "utf8");
  console.log(`Synced ${Object.keys(parsed.platforms || {}).length} platforms (${bytes} bytes) into index.html.`);
}
